                <ion-grid>
                    <ion-row>
                        <ion-col size="6" size-xm="6" v-for="(prod, i) in this.$store.state.productos" :key="i" >
                            <!-- TARJETAS PRODUCTOS -->
                            <ion-card v-if="prod.categoria === this.$route.params.categoria">
                                <img alt="Silhouette of mountains" style="width: auto; height: auto;"
