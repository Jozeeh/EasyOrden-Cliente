                                </ion-card-header>

                                <ion-card-content>
                                    <b>Descripcion: </b> {{ prod.descripcion }}
                                    <br>
                                    Precio: <b>${{ prod.precio }} </b>
                                    <br>
                                    <!-- AGREGAR AL CARRITO -->
                                    <ion-button color="success" class="btnAgregarCarrito" @click="agregarCarrito(i)">
                                        Agregar
                                        <ion-icon aria-hidden="true" slot="start" :ios="cart" :md="cart"></ion-icon>
                                    </ion-button>
                                </ion-card-content>
                            </ion-card>
                        </ion-col>

